# --- Server -------------------------------------------------------------------

def dispatch(host: str, path: str):
    """Return (status, headers, body) for the first matching scenario."""
    for host_re, path_re, fn in SCENARIOS:
        if host_re.match(host) and path_re.match(path):
            return fn(path)
    # Cloudflare-fronted heuristic (only if host contains 'cf' or explicit path).
    if "cloudflare" in host.lower() or path.startswith("/cloudflare"):
        return cloudflare_generic(path)
    return 200, {"Content-Type": "text/plain"}, "cloudintel demo store: no matching scenario for host " + host


class Handler(http.server.BaseHTTPRequestHandler):
    def _handle(self):
        host = self.headers.get("Host", "")
        # Strip port from host header.
        if ":" in host and not host.startswith("["):
            host = host.split(":", 1)[0]
        status, headers, body = dispatch(host, self.path)
        self.send_response(status)
        for k, v in headers.items():
            self.send_header(k, v)
        self.send_header("Content-Length", str(len(body.encode("utf-8"))))
        self.end_headers()
        self.wfile.write(body.encode("utf-8"))

    def do_GET(self):
        self._handle()

    def do_HEAD(self):
        self._handle()

    def log_message(self, fmt, *args):
        sys.stderr.write("%s - %s\n" % (self.address_string(), fmt % args))


class ReuseTCPServer(socketserver.ThreadingTCPServer):
    allow_reuse_address = True


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--port", type=int, default=8787)
    p.add_argument("--host", default="127.0.0.1")
    args = p.parse_args()
    server = ReuseTCPServer((args.host, args.port), Handler)
    print(f"CloudIntel demo store listening on http://{args.host}:{args.port}")
    print(f"Registered scenarios: {len(SCENARIOS)}")
    print("Tip: run drive.py to fire every scenario through Burp automatically.")
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        server.shutdown()


if __name__ == "__main__":
    main()
