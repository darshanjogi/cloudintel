#!/usr/bin/env python3
"""
CloudIntel local demo store.

A tiny HTTP server that serves synthetic responses reproducing the real signals every
CloudIntel fingerprint looks for (headers, error XML, cookies). Point Burp Suite at it as
a proxy target, load the CloudIntel extension, then run `drive.py` (or curl commands from
its docs) to exercise all 40 fingerprints locally — no internet, no real cloud accounts.

Routing:
- The server keys off the request's `Host:` header, not the connection target. That way you
  can send requests to `http://127.0.0.1:8787/` while using a real cloud hostname in `Host:`
  (via `curl --resolve` or Burp's target overrides), and CloudIntel sees the genuine host.
- Alternatively, hit `/<service>/...` directly — the URL path selects the response as a
  fallback when the Host header is generic.

Usage:
    python3 server.py                 # http://127.0.0.1:8787
    python3 server.py --port 9000     # custom port
    python3 drive.py                  # optional: fire every scenario through Burp
"""

