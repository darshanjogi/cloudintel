# --- Response registry --------------------------------------------------------

# Each entry: (host regex, url-path regex, handler(name)->(status, headers, body))
# The first matching entry wins.
Handler = Callable[[str], tuple]  # (status, headers dict, body str)

SCENARIOS: list[tuple[re.Pattern, re.Pattern, Handler]] = []


def scenario(host_pat: str, path_pat: str = ".*"):
    def deco(fn):
        SCENARIOS.append((re.compile(host_pat, re.I), re.compile(path_pat, re.I), fn))
        return fn
    return deco


